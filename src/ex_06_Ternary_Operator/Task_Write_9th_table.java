package ex_06_Ternary_Operator;

public class Task_Write_9th_table {
    public static void main(String[] args) {
        int n= Integer.parseInt (args[0]);
        //System.out.println( "9 x 1= %d\n", number*1);
        System.out.printf("%dx1=%d\n",n,n*1);
        System.out.printf("%dx2=%d\n",n,n*2);
        System.out.printf("%dx3=%d",n,n*3);
        System.out.println();
        System.out.printf("%dx4=%d",n,n*4).println();
        System.out.printf("%dx5=%d",n,n*5).println();;
        System.out.printf("%dx6=%d",n,n*6).println();;
        System.out.printf("%dx7=%d",n,n*7);
        System.out.println();
        System.out.printf("%dx8=%d",n,n*8);
        System.out.println();
        System.out.printf("%dx9=%d",n,n*9);









    }
}
